public class ThreeConst
	{
		 public static void main(String [] args) 
		{
			new ThreeConst(4L);
		}
		public void ThreeConst(int x) 
		{
			System.out.print(" " + (x * 2));
		}
		public void ThreeConst(long x) 
		{
			System.out.print(" " + x);
		 }
		public void ThreeConst() 
		{
			System.out.print("no-arg ");
		}
	}
